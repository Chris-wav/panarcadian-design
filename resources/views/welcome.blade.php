      const overlay = document.querySelector(".mobile-overlay");
      const closeBtn = document.querySelector(".close-menu");
      const hasSub = document.querySelector(".has-sub");
      const submenu = document.querySelector(".mobile-submenu");
      const backBtn = document.querySelector(".back-button");
      const mobileHeader = document.querySelector(".mobile-header");
      const mobileAuth = document.querySelector(".mobile-auth");
      hamburger.addEventListener("click", () => {
        mobileMenuMain.classList.toggle("active");
        mobileMenu.classList.toggle("active");
        overlay.classList.toggle("active");
      });

      closeBtn.addEventListener("click", closeMenu);
      overlay.addEventListener("click", closeMenu);

      function closeMenu() {
        mobileMenu.classList.remove("active");
        overlay.classList.remove("active");
        submenu.classList.remove("active");
      }

      hasSub.addEventListener("click", () => {
        submenu.classList.add("active");
        mobileMenuMain.classList.remove("active");
        mobileHeader.classList.add("hidden");
        mobileAuth.classList.add("hidden");
      });

      backBtn.addEventListener("click", () => {
        submenu.classList.remove("active");
        mobileMenu.classList.remove("hidden");
        mobileHeader.classList.remove("hidden");
        mobileAuth.classList.remove("hidden");
        mobileMenuMain.classList.add("active");
        mobileHeader.classList.add("active");
        mobileAuth.classList.add("active");
